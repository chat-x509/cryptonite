static void des_ecb_core(DesTestHelper *test_data)
{
    ByteArray *key = ba_alloc_from_le_hex_string(test_data->key);
    ByteArray *data = ba_alloc_from_le_hex_string(test_data->data);
    ByteArray *expected = ba_alloc_from_le_hex_string(test_data->exp);
    ByteArray *actual = NULL;
    ByteArray *decrypted = NULL;
    DesCtx *ctx = NULL;
    size_t count = error_count;

    ctx = des_alloc();
    ASSERT_NOT_NULL(ctx);

    ASSERT_RET_OK(des_init_ecb(ctx, key));
    ASSERT_RET_OK(des_encrypt(ctx, data, &actual));

    CHECK_EQUALS_BA(expected, actual);

    ASSERT_RET_OK(des_decrypt(ctx, actual, &decrypted));

    CHECK_EQUALS_BA(data, decrypted);

cleanup:

    PRINT_ERROR(print_cipher_error("DES ECB", key, NULL, data), count);

    BA_FREE(key, data, expected, actual, decrypted);
    des_free(ctx);
}

