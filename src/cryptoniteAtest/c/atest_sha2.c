static void loop_hmac(Sha2Variant id, int i, const uint8_t *expected, size_t len)
{
    ByteArray *input_data_ba = NULL;
    ByteArray *key_ba = NULL;
    ByteArray *expected_ba = NULL;
    ByteArray *mac = NULL;
    HmacCtx *ctx = NULL;
    size_t count = error_count;

    key_ba = ba_alloc_from_uint8(&test_sha2_key[i][1], test_sha2_key[i][0]);
    input_data_ba = ba_alloc_from_uint8(&test_sha2_hmac_data[i][1], test_sha2_hmac_data[i][0]);

    expected_ba = ba_alloc_from_uint8(expected, len);

    ASSERT_NOT_NULL(ctx = hmac_alloc_sha2(id));
    ASSERT_RET_OK(hmac_init(ctx, key_ba));
    ASSERT_RET_OK(hmac_update(ctx, input_data_ba));
    ASSERT_RET_OK(hmac_final(ctx, &mac));

    CHECK_EQUALS_BA(expected_ba, mac);

cleanup:

    PRINT_ERROR(print_hmac_error("SHA2", key_ba, input_data_ba), count);

    hmac_free(ctx);
    BA_FREE(input_data_ba, key_ba, expected_ba, mac);
}

void atest_sha2(void)
{
    size_t err_count = error_count;
    int i;

    test_sha2();

    for (i = 0; i < 3; i++) {
        loop_hmac(SHA2_VARIANT_224, i, test_sha224_hmac_expected[i], 28);
        loop_hmac(SHA2_VARIANT_256, i, test_sha256_hmac_expected[i], 32);
        loop_hmac(SHA2_VARIANT_384, i, test_sha384_hmac_expected[i], 48);
        loop_hmac(SHA2_VARIANT_512, i, test_sha512_hmac_expected[i], 64);
    }

    if (err_count == error_count) {
        msg_print_atest("SHA2", "[hash-224,256,384,512,hmac-224,256,384,512]", "OK");
    } else {
        msg_print_atest("SHA2", "", "FAILED");
    }
}
