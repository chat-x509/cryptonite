void  test_sha2_core(Sha2Variant type, const uint8_t *data, size_t data_len, const uint8_t *expected,
        size_t expected_len)
{
    Sha2Ctx *ctx = NULL;
    ByteArray *expected_ba = NULL;
    ByteArray *actual_ba = NULL;
    ByteArray *data_ba = NULL;
    ByteArray *print_data_ba = NULL;
    size_t count = error_count;
    size_t len[3];
    size_t i;
    size_t offset = 0;

    len[0] = 1;
    len[1] = 1;
    len[2] = data_len - 2;

    expected_ba = ba_alloc_from_uint8(expected, expected_len);
    print_data_ba = ba_alloc_from_uint8(data, data_len);

    ctx = sha2_alloc(type);
    ASSERT_NOT_NULL(ctx);

    for (i = 0; i < (sizeof(len) / sizeof(len[0])); i++) {
        data_ba = ba_alloc_from_uint8(&data[offset], len[i]);
        ASSERT_RET_OK(sha2_update(ctx, data_ba));

        offset += len[i];

        ba_free(data_ba);
        data_ba = NULL;
    }

    ASSERT_RET_OK(sha2_final(ctx, &actual_ba));

    CHECK_EQUALS_BA(expected_ba, actual_ba);

cleanup:

    PRINT_ERROR(print_hash_error("SHA2", print_data_ba), count);

    sha2_free(ctx);

    ba_free(actual_ba);
    ba_free(expected_ba);
    ba_free(data_ba);
    ba_free(print_data_ba);
}

