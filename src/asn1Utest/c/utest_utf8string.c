#include "test_utils.h"
#include "utest_asn1.h"

static UTF8String_t *test_utf8string_create(void)
{
    uint8_t encode[15] = {
        0x0c, 0x0d,
        0x54, 0x65, 0x73, 0x74,
        0x20, 0x6d, 0x65, 0x73,
        0x73, 0x61, 0x67, 0x65,
        0x2e
    };

    UTF8String_t *utf8string = NULL;

    ASN_EXECUTE(ber_decode(0, &UTF8String_desc, (void *)&utf8string, encode, sizeof(encode)));

    ASSERT_NOT_NULL(utf8string);
cleanup:
    return utf8string;
}

void test_utf8string_alloc_free(void)
{
    UTF8String_t *utf8string = NULL;

    ASSERT_ASN_ALLOC(utf8string);
    ASSERT_NOT_NULL(utf8string);
cleanup:

    ASN_FREE(&UTF8String_desc, utf8string);
}

void test_utf8string_der_encode(void)
{
    ByteArray *actual = NULL;
    ByteArray *expected = NULL;
    uint8_t exp[15] = {
        0x0c, 0x0d,
        0x54, 0x65, 0x73, 0x74,
        0x20, 0x6d, 0x65, 0x73,
        0x73, 0x61, 0x67, 0x65,
        0x2e
    };

    UTF8String_t *utf8string = NULL;

    utf8string = test_utf8string_create();
    ASSERT_NOT_NULL(utf8string);
    ASSERT_NOT_NULL(expected = ba_alloc_from_uint8(exp, sizeof(exp)));

    ASSERT_RET_OK(asn_encode_ba(&UTF8String_desc, utf8string, (void *)&actual));
    ASSERT_NOT_NULL(actual);

    ASSERT_EQUALS_BA(expected, actual);

cleanup:
    BA_FREE(expected, actual);
    ASN_FREE(&UTF8String_desc, utf8string);
}

